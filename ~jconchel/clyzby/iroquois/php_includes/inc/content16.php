<h3>Funeral Addresses</h3>

<p>108. At the funeral of a Lord of the Confederacy, say: Now we become reconciled as you start away. You were once a
  Lord of the Five Nations' Confederacy and the United People trusted you. Now we release you for it is true that it is
  no longer possible for us to walk about together on the earth. Now, therefore, we lay it (the body) here. Here we lay
  it away. Now then we say to you, 'Persevere onward to the place where the Creator dwells in peace. Let not the things
  of the earth hinder you. Let nothing that transpired while yet you lived hinder you. In hunting you once took delight;
  in the game of Lacrosse you once took delight and in the feasts and pleasant occasions your mind was amused, but now
  do not allow thoughts of these things to give you trouble. Let not your relatives hinder you and also let not your
  friends and associates trouble your mind. Regard none of these things.'</p>
<p>"Now then, in turn, you here present who were related to this man and you who were his friends and associates, behold
  the path that is yours also! Soon we ourselves will be left in that place. For this reason hold yourselves in
  restraint as you go from place to place. In your actions and in your conversation do no idle thing. Speak not idle
  talk neither gossip. Be careful of this and speak not and do not give way to evil behavior. One year is the time that
  you must abstain from unseemly levity but if you can not do this for ceremony, ten days is the time to regard these
  things for respect."</p>
<p>109. At the funeral of a War Chief, say:</p>
<blockquote>
  <p>"Now we become reconciled as you start away. You were once a War Chief of the Five Nations' Confederacy and the
    United People trusted you as their guard from the enemy." (The remainder is the same as the address at the funeral
    of a Lord).</p>
</blockquote>
<p>110. At the funeral of a Warrior, say:</p>
<blockquote>
  <p>"Now we become reconciled as you start away. Once you were a devoted provider and protector of your family and you
    were ever ready to take part in battles for the Five Nations' Confederacy. The United People trusted you." (The
    remainderis the same as the address at the funeral of a Lord).</p>
</blockquote>
<p>111. At the funeral of a young man, say:</p>
<blockquote>
  <p>"Now we become reconciled as you start away. In the beginning of your career you are taken away and the flower of
    your life is withered away." (The remainder is the same as the address at the funeral of a Lord).</p>
</blockquote>
<p>112. At the funeral of a chief woman, say:</p>
<blockquote>
  <p>"Now we become reconciled as you start away. You were once a chief woman in the Five Nations' Confederacy. You once
    were a mother of the nations. Now we release you for it is true that it is no longer possible for us to walk about
    together on the earth. Now, therefore, we lay it (the body) here. Here we lay it away. Now then we say to you,
    'Persevere onward to the place where the Creator dwells in peace. Let not the things of the earth hinder you. Let
    nothing that transpired while you lived hinder you. Looking after your family was a sacred duty and you were
    faithful. You were one of the many joint heirs of the Lordship titles. Feastings were yours and you had pleasant
    occasions. . ." (The remainder is the same as the address at the funeral of a Lord).</p>
</blockquote>
<p>113. At the funeral of a woman of the people, say:</p>
<blockquote>
  <p>"Now we become reconciled as you start away. You were once a woman in the flower of life and the bloom is now
    withered away. You once held a sacred position as a mother of the nation. (Etc.) Looking after your family was a
    sacred duty and you were faithful. Feastings . . . (etc.)" (The remainder is the same as the address at the funeral
    of a Lord).</p>
</blockquote>
<p>114. At the funeral of an infant or young woman, say:</p>
<blockquote>
  <p>"Now we become reconciled as you start away. You were a tender bud and gladdened our hearts for only a few days.
    Now the bloom has withered away . . . (etc.) Let none of the things that transpired on earth hinder you. Let nothing
    that happened while you lived hinder you." (The remainder is the same as the address at the funeral of a Lord).</p>
</blockquote>
<p>115. When an infant dies within three days, mourning shall continue only five days. Then shall you gather the little
  boys and girls at the house of mourning and at the funeral feast a speaker shall address the children and bid them be
  happy once more, though by a death, gloom has been cast over them. Then shall the black clouds roll away and the sky
  shall show blue once more. Then shall the children be again in sunshine.</p>
<p>116. When a dead person is brought to the burial place, the speaker on the opposite side of the Council Fire shall
  bid the bereaved family cheer their minds once again and rekindle their hearth fires in peace, to put their house in
  order and once again be in brightness for darkness has covered them. He shall say that the black clouds shall roll
  away and that the bright blue sky is visible once more. Therefore shall they be in peace in the sunshine again.</p>
<p>117. Three strings of shell one span in length shall be employed in addressing the assemblage at the burial of the
  dead. The speaker shall say:</p>
<blockquote>
  <p>"Hearken you who are here, this body is to be covered. Assemble in this place again ten days hence for it is the
    decree of the Creator that mourning shall cease when ten days have expired. Then shall a feast be made."</p>
</blockquote>
<p>Then at the expiration of ten days the speaker shall say:</p>
<blockquote>
  <p>"Continue to listen you who are here. The ten days of mourning have expired and your minds must now be freed of
    sorrow as before the loss of a relative. The relatives have decided to make a little compensation to those who have
    assisted at the funeral. It is a mere expression of thanks. This is to the one who did the cooking while the body
    was lying in the house. Let her come forward and receive this gift and be dismissed from the task."</p>
</blockquote>
<p>In substance this shall be repeated for every one who assisted in any way until all have been remembered.</p>
